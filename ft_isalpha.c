#include<stdio.h>

int	ft_isalpha(int a)
{
	if ((a >= 65) && (a <= 90) || (a >= 97) && (a <= 122))
	{
		return (1);
	}
	return (0);
}

int	main(void)
{
	int	a;

	a = 'h';
	printf("%d", ft_isalpha(a));
}
