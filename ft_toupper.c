#include<stdio.h>

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		c -= 32;
	return (c);
}

int	main(void)
{
	int	a;

	a = 'a';
	printf("%c", ft_toupper(a));
}
