#include<stdio.h>

int	ft_tolower(int a)
{
	if (a >= 'A' && a <= 'Z')
		a += 32;
	return (a);
}

int	main(void)
{
	int	a;

	a = 'C';
	printf("%c", ft_tolower(a));
}
